export function translateText(text: string, locale: Locale): string {
  if (locale === "en") {
    return text;
  }

  if (frExact[text]) {
    return frExact[text];
  }

  for (const [pattern, replacer] of frRules) {
    const match = text.match(pattern);
    if (match) {
      return replacer(...match);
    }
  }

  return text;
}
